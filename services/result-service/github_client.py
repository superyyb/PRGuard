import httpx
import os

GITHUB_TOKEN = os.getenv("GITHUB_TOKEN", "")

HEADERS = {
    "Authorization": f"token {GITHUB_TOKEN}",
    "Accept": "application/vnd.github.v3+json",
}


def post_pr_comment(repo_full_name: str, pr_number: int, body: str):
    url = f"https://api.github.com/repos/{repo_full_name}/issues/{pr_number}/comments"
    response = httpx.post(url, headers=HEADERS, json={"body": body})
    response.raise_for_status()
    return response.json()


def format_ai_comment(review: dict) -> str:
    score = review.get("score", "N/A")
    summary = review.get("summary", "")
    approved = review.get("approved", False)
    issues = review.get("issues", [])
    suggestions = review.get("suggestions", [])

    status = "✅ Approved" if approved else "❌ Changes Requested"
    emoji = "🟢" if score >= 7 else "🟡" if score >= 4 else "🔴"

    lines = [
        "## 🤖 AI Code Review",
        "",
        f"**Status:** {status}  |  **Score:** {emoji} {score}/10",
        "",
        f"**Summary:** {summary}",
    ]

    if issues:
        lines += ["", "### Issues Found"]
        severity_icon = {"high": "🔴", "medium": "🟡", "low": "🔵"}
        for issue in issues:
            icon = severity_icon.get(issue["severity"], "⚪")
            lines.append(f"- {icon} **{issue['severity'].upper()}** `{issue['line']}` — {issue['comment']}")

    if suggestions:
        lines += ["", "### Suggestions"]
        for s in suggestions:
            lines.append(f"- 💡 {s}")

    lines += ["", "---", "*Generated by AI Review Worker*"]
    return "\n".join(lines)


def format_security_comment(findings: list, passed: bool) -> str:
    status = "✅ No issues found" if passed else f"⚠️ {len(findings)} issue(s) found"

    lines = [
        "## 🔒 Security Scan",
        "",
        f"**Status:** {status}",
    ]

    if findings:
        lines += ["", "### Findings"]
        severity_icon = {"high": "🔴", "medium": "🟡", "low": "🔵"}
        for f in findings:
            icon = severity_icon.get(f["severity"], "⚪")
            lines.append(f"- {icon} **{f['severity'].upper()}** Line {f['line']}: {f['message']}")
            lines.append(f"  ```\n  {f['content']}\n  ```")

    lines += ["", "---", "*Generated by Security Scanner*"]
    return "\n".join(lines)
